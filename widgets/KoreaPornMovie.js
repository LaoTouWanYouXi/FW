async function loadPage(params = {}) {
  try {
    let url = params.url || BASE_URL + "/";
    const page = params.page || "1";
    if (page && page !== "1") {
      // WordPress \u5206\u9875: /page/{n}/
      const base = url.replace(/\/page\/\d+\/?$/, "").replace(/\/?$/, "/");
      url = base + "page/" + page + "/";
    }
    const { data } = await Widget.http.get(url, { headers: getHeaders(BASE_URL + "/") });
    return parseListHtml(data || "");
  } catch (e) {
    return [];
  }
}

async function search(params = {}) {
  try {
    if (!params.keyword?.trim()) return [];
    const keyword = encodeURIComponent(params.keyword.trim());
    const page = params.page || "1";
    let url = `${BASE_URL}/page/${page}/?s=${keyword}`;
    if (page === "1") url = `${BASE_URL}/?s=${keyword}`;

    const { data } = await Widget.http.get(url, { headers: getHeaders(BASE_URL + "/") });
    return parseListHtml(data || "");
  } catch (e) {
    return [];
  }
}

